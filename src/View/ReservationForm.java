    private void imprimer(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimer
        Reservation reservation=res.findById(id);
        String nom = reservation.getClt().getNom();
        String prenom = reservation.getClt().getPrenom();
        String chambre = reservation.getCh().getCateg().getLibelle();
        String dateDebut=reservation.getDateDebut().toString();
        String dateFin= reservation.getDateFin().toString();
        System.out.println("Client: "+nom+" "+prenom+"\nChambre: "+chambre+"\nDate debut: "+dateDebut+"\nDate fin: "+dateFin);
        Document document = new Document();
        try {
            PdfWriter.getInstance(document, new FileOutputStream(nom+prenom+".pdf"));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        document.open();
        try {
            document.add(new Paragraph("Client: "+nom+" "+prenom+"\nChambre: "+chambre+"\nDate debut: "+dateDebut+"\nDate fin: "+dateFin));
        } catch (DocumentException ex) {
            Logger.getLogger(ReservationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        document.close();
        JOptionPane.showMessageDialog(this, "recu bien imprimé");
    }//GEN-LAST:event_imprimer


