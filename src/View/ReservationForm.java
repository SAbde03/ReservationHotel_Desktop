    private void ajouterReservation(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterReservation
     
        Date dateDebut =jDateChooser1.getDate();
       Date dateFin=jDateChooser2.getDate();
       Chambre ch=(Chambre) jComboBox2.getSelectedItem();
       Client clt=(Client) jComboBox1.getSelectedItem();
       Reservation reservation=new Reservation(dateDebut,dateFin,clt,ch);
       int rep=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment ajouter cette reservation");
                   if(rep==JOptionPane.YES_OPTION){
                       if(res.create(reservation)){
                           JOptionPane.showMessageDialog(this, "reservation bien ajouté");
                           afficherReservation();
                       }
                   }
    }//GEN-LAST:event_ajouterReservation

    private void modifierReservation(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierReservation
       Date dateDebut =jDateChooser1.getDate();
       Date dateFin=jDateChooser2.getDate();
       Chambre ch=(Chambre) jComboBox2.getSelectedItem();
       Client clt=(Client) jComboBox1.getSelectedItem();
       Reservation reservation=res.findById(id);
       reservation.setDateDebut(dateDebut);
       reservation.setDateFin(dateFin);
       reservation.setCh(ch);
       reservation.setClt(clt);
       int rep=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier cette réservation");
                   if(rep==JOptionPane.YES_OPTION){
                       if(res.update(reservation)){
                           JOptionPane.showMessageDialog(this, "réservation bien modifiée");
                           afficherReservation();
                       }
                   }
    }//GEN-LAST:event_modifierReservation

