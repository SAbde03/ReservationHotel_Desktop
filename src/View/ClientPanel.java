    private void jButton1AjouterClient(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1AjouterClient
        String nom=jTextField1.getText();
        String prenom=jTextField2.getText();
        String tel=jTextField3.getText();
        String mail=jTextField4.getText();
        Client cl=new Client(nom,prenom,tel,mail);
        int rep=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment ajouter ce client");
        if(rep==JOptionPane.YES_OPTION){
            if(cs.create(cl)){
                JOptionPane.showMessageDialog(this, "Client bien ajouté");
                afficherClient();
            }
        }
    }//GEN-LAST:event_jButton1AjouterClient

