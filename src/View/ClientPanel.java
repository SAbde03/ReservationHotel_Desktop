    public void afficherClient(){
        model.setRowCount(0);
        
        for(Client c:cs.findAll()){
            model.addRow(new Object[]{
                c.getId(),
                c.getNom(),
                c.getPrenom(),
                c.getTelephone(),
                c.getEmail(),
            });
        }
    }
