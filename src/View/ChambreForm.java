    private void ajouterChambre(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterChambre
        String telephone=jTextField1.getText();
       Categorie cate=(Categorie) jComboBox1.getSelectedItem();
       Chambre ch=new Chambre(telephone,cate);
       int rep=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment ajouter cette chambre");
                   if(rep==JOptionPane.YES_OPTION){
                       if(chs.create(ch)){
                           JOptionPane.showMessageDialog(this, "Chambre bien ajouté");
                           afficherChambre();
                       }
                   }
    }//GEN-LAST:event_ajouterChambre

    private void supprimerChambre(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerChambre
        Chambre ch=chs.findById(id);
        int rep=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer ce client");
                   if(rep==JOptionPane.YES_OPTION){
                       if(chs.delete(ch)){
                           JOptionPane.showMessageDialog(this, "Client bien supprimé");
                           afficherChambre();
                       }
                      
                   }
    }//GEN-LAST:event_supprimerChambre

