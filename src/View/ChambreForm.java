    private void ModifierChambre(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierChambre
       String telephone=jTextField1.getText();
       Categorie cate=(Categorie) jComboBox1.getSelectedItem();
       Chambre ch=chs.findById(id);
       ch.setTelephone(telephone);
       ch.setCateg(cate);
       int rep=JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier cette chambre");
                   if(rep==JOptionPane.YES_OPTION){
                       if(chs.update(ch)){
                           JOptionPane.showMessageDialog(this, "Chambre bien modifié");
                           afficherChambre();
                       }
                   }
    }//GEN-LAST:event_ModifierChambre

